package com.hello;

/**
 * 常量：是指在java程序中固定不变的数量
 */
public class ConstDemo {
    public static void main(String[] args) {
        //整数常量
        System.out.println(123);
        //小数常量
        System.out.println(4.5);
        //字符常量
        System.out.println('A');
        //字符串常量
        System.out.println("hello 您好");
        //布尔常量
        System.out.println(true);
        //空常量
    }
}
